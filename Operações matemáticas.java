package catherine;

import java.util.Scanner;

public class Aula02Exercício3 {

	public static void main(String[] args) {
		int horas, minutos, segundos, soma;
		String nome; 
		
		Scanner teclado = new Scanner(System.in);
		
		System.out.print("Qual o seu nome? "); nome = teclado.nextLine();
		System.out.print("Qual a hora atual? "); horas = teclado.nextInt();
		System.out.print("Qual o minuto atual? "); minutos = teclado.nextInt();
		System.out.print("Qual o segundo atual? " ); segundos = teclado.nextInt();
		
        soma = (3600 * horas) + (60 * minutos) + segundos;
         
        
        System.out.print("Se passaram " + soma + " segundos");
        
        teclado.close();
        
		
	}

}









































































































































































